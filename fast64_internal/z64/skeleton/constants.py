def get_skeleton_import_dict(game_mode: str | None) -> OrderedDict[str, OOTSkeletonImportInfo]:
    return mmSkeletonImportDict if game_mode == "MM" else ootSkeletonImportDict


def find_skeleton_import_info(mode: str, game_mode: str | None) -> OOTSkeletonImportInfo | None:
    import_dict = get_skeleton_import_dict(game_mode)
    return import_dict.get(mode)


def get_skeleton_mode_items(self, context) -> list[tuple[str, str, str]]:
    items = [("Generic", "Generic", "Generic")]
    scene = getattr(context, "scene", None)
    if scene is None:
        return items
    game_mode = getattr(scene, "gameEditorMode", "OOT")
    import_dict = get_skeleton_import_dict(game_mode)
    items.extend((name, name, name) for name in import_dict.keys())
    return items
